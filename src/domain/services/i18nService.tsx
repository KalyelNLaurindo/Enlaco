// React context interface for active internationalization state
interface I18nContextProps {
  language: Language;
  setLanguage: (lang: Language) => void;
  t: (key: keyof typeof translations['pt']) => string;
}

const I18nContext = createContext<I18nContextProps | undefined>(undefined);

// Context provider component that wraps the application and sets active language
export const I18nProvider: React.FC<{ children: React.ReactNode }> = ({ children }) => {
  const [language, setLanguageState] = useState<Language>(() => {
    const saved = localStorage.getItem('enlaco_lang') as Language;
    return saved && translations[saved] ? saved : 'pt';
  });

  const setLanguage = (lang: Language) => {
    setLanguageState(lang);
    localStorage.setItem('enlaco_lang', lang);
  };

  const t = (key: keyof typeof translations['pt']): string => {
    const dict = translations[language] || translations['pt'];
    return dict[key] || translations['pt'][key] || String(key);
  };

  return (
    <I18nContext.Provider value={{ language, setLanguage, t }}>
      {children}
    </I18nContext.Provider>
  );
};

// Utility hook to access translations reactively in components.
// Returns a safe fallback object when called outside the provider context (e.g. in unit tests).
export const useTranslation = () => {
  const context = useContext(I18nContext);
  if (!context) {
    return {
      language: 'pt' as Language,
      setLanguage: () => {},
      t: (key: keyof typeof translations['pt']): string => {
        return translations['pt'][key] || String(key);
      }
    };
  }
  return context;
};
